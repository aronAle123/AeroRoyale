    private void configurarEventos() {
        jTextField13.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                actualizarResumen();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                actualizarResumen();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                actualizarResumen();
            }
        });
    }

    private void actualizarResumen() {
        if (vueloSeleccionado == null) {
            return;
        }
        double precio = vueloSeleccionado.getPrecio();
        jTextField14.setText(String.format(java.util.Locale.US, "%.2f", precio));

        int cantidad = 0;
        try {
            cantidad = Integer.parseInt(jTextField13.getText().trim());
        } catch (NumberFormatException ignored) {
        }
        if (cantidad < 0) {
            cantidad = 0;
        }

        jTextField15.setText(String.valueOf(cantidad));
        jTextField16.setText(String.format(java.util.Locale.US, "%.2f", precio * cantidad));
    }

    public void setVuelo(Vuelo vuelo) {
        this.vueloSeleccionado = vuelo;
        if (vuelo == null) {
            return;
        }
        jTextField1.setText(vuelo.getCodigo());
        jTextField2.setText(vuelo.getRuta() != null ? vuelo.getRuta().getOrigen() : "");
        jTextField3.setText(vuelo.getRuta() != null ? vuelo.getRuta().getDestino() : "");
        jFormattedTextField1.setText(vuelo.getFecha());
        jTextField4.setText(vuelo.getHora());
        jTextField5.setText(vuelo.getAvion() != null ? vuelo.getAvion().getCodigo() : "");
        jTextField6.setText(vuelo.getAvion() != null ? vuelo.getAvion().getModelo() : "");
        jTextField7.setText(vuelo.getAvion() != null ? String.valueOf(vuelo.getAvion().getCapacidad()) : "");
        jTextField8.setText(String.valueOf(vuelo.getCuposDisponibles()));
        jTextField9.setText(String.valueOf(vuelo.getPrecio()));
        if (jTextField13.getText().trim().isEmpty()) {
            jTextField13.setText("1");
        }
        actualizarResumen();
    }

    private void actualizarVueloDespuesCompra(int cantidad) {
        int nuevoCupo = vueloSeleccionado.getCuposDisponibles() - cantidad;
        vueloSeleccionado.setCuposDisponibles(nuevoCupo);
        jTextField8.setText(String.valueOf(nuevoCupo));
        actualizarResumen();
    }

