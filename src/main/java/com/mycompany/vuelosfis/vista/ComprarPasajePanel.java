    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (vueloSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un vuelo.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String cedula = jTextField10.getText().trim();
        String nombre = jTextField11.getText().trim();
        String correo = jTextField12.getText().trim();
        String cantidadTxt = jTextField13.getText().trim();

        int cantidad;
        try {
            cantidad = Integer.parseInt(cantidadTxt);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Cantidad de asientos inválida.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(this, "La cantidad de asientos debe ser mayor a cero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cantidad > 6) {
            JOptionPane.showMessageDialog(this, "La cantidad máxima es 6 asientos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            Pasajero pasajero = new Pasajero(cedula, nombre, correo);
            reservaService.crearReserva(vueloSeleccionado, pasajero, EstadoReserva.COMPRADO, cantidad);
            actualizarVueloDespuesCompra(cantidad);
            JOptionPane.showMessageDialog(this, "Compra realizada correctamente.", "OK", JOptionPane.INFORMATION_MESSAGE);
            if (onCompra != null) {
                onCompra.accept(cedula);
            }
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

