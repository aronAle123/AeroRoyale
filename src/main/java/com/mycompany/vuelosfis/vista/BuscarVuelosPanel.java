    private void cargarTabla(List<Vuelo> vuelos) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        vuelosActuales.clear();
        for (Vuelo v : vuelos) {
            String origen = v.getRuta() != null ? v.getRuta().getOrigen() : "";
            String destino = v.getRuta() != null ? v.getRuta().getDestino() : "";
            String avionCodigo = v.getAvion() != null ? v.getAvion().getCodigo() : "";
            String avionModelo = v.getAvion() != null ? v.getAvion().getModelo() : "";
            Integer capacidad = v.getAvion() != null ? v.getAvion().getCapacidad() : null;
            model.addRow(new Object[]{
                v.getCodigo(),
                origen,
                destino,
                v.getFecha(),
                v.getHora(),
                avionCodigo,
                avionModelo,
                capacidad,
                v.getCuposDisponibles(),
                v.getPrecio(),
                "Seleccionar"
            });
            vuelosActuales.add(v);
        }
    }

    private void configurarTabla() {
        DefaultTableModel model = new DefaultTableModel(
                new Object[]{"Codigo", "Origen", "Destino", "Fecha", "Hora", "Codigo Avion", "Modelo Avion", "Capacidad", "Cupo", "Precios", "Accion"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 10;
            }
        };
        jTable1.setModel(model);
        jTable1.setRowHeight(28);
        jTable1.getColumnModel().getColumn(10).setCellRenderer(new ButtonRenderer());
        jTable1.getColumnModel().getColumn(10).setCellEditor(new ButtonEditor());
    }

    private class ButtonRenderer extends JButton implements TableCellRenderer {
        public ButtonRenderer() {
            setText("Seleccionar");
        }

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            setText(value != null ? value.toString() : "Seleccionar");
            return this;
        }
    }

    private class ButtonEditor extends DefaultCellEditor {
        private final JButton button = new JButton("Seleccionar");
        private int row;

        public ButtonEditor() {
            super(new javax.swing.JTextField());
            button.addActionListener(evt -> {
                seleccionarVuelo(row);
                fireEditingStopped();
            });
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            this.row = row;
            button.setText(value != null ? value.toString() : "Seleccionar");
            return button;
        }

        @Override
        public Object getCellEditorValue() {
            return "Seleccionar";
        }
    }

    private void seleccionarVuelo(int row) {
        if (row < 0 || row >= vuelosActuales.size()) {
            return;
        }
        Vuelo seleccionado = vuelosActuales.get(row);
        if (onSeleccionar != null) {
            onSeleccionar.accept(seleccionado);
        }
    }

