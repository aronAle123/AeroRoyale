    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Contenedor.removeAll();
        Contenedor.setLayout(new BorderLayout());
        Contenedor.add(new BuscarVuelosPanel(this::mostrarComprarPasaje), BorderLayout.CENTER);
        Contenedor.revalidate();
        Contenedor.repaint();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void mostrarComprarPasaje(Vuelo vuelo) {
        Contenedor.removeAll();
        Contenedor.setLayout(new BorderLayout());
        ComprarPasajePanel panel = new ComprarPasajePanel(this::mostrarReservas, this::mostrarBusqueda);
        panel.setVuelo(vuelo);
        Contenedor.add(panel, BorderLayout.CENTER);
        Contenedor.revalidate();
        Contenedor.repaint();
    }

    private void mostrarBusqueda() {
        Contenedor.removeAll();
        Contenedor.setLayout(new BorderLayout());
        Contenedor.add(new BuscarVuelosPanel(this::mostrarComprarPasaje), BorderLayout.CENTER);
        Contenedor.revalidate();
        Contenedor.repaint();
    }

    private void mostrarReservas(String cedula) {
        if (cedula == null || cedula.isBlank()) {
            cedula = JOptionPane.showInputDialog(this, "Ingrese la c√©dula del pasajero:");
        }
        if (cedula == null || cedula.isBlank()) {
            return;
        }
        ultimoCedula = cedula.trim();
        reservasPanel.setCedulaFiltro(ultimoCedula);
        Contenedor.removeAll();
        Contenedor.setLayout(new BorderLayout());
        Contenedor.add(reservasPanel, BorderLayout.CENTER);
        Contenedor.revalidate();
        Contenedor.repaint();
    }

